package employerdetails;

public class EmployerValues {
	String emName;
	String emAddress;
	String emDepart;
	int emNOE;
	public String getEmName() {
		return emName;
	}
	public void setEmName(String emName) {
		this.emName = emName;
	}
	public String getEmAddress() {
		return emAddress;
	}
	public void setEmAddress(String emAddress) {
		this.emAddress = emAddress;
	}
	public String getEmDepart() {
		return emDepart;
	}
	public void setEmDepart(String emDepart) {
		this.emDepart = emDepart;
	}
	public int getEmNOE() {
		return emNOE;
	}
	public void setEmNOE(int emNOE) {
		this.emNOE = emNOE;
	}
	

}
